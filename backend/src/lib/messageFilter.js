/**
 * Проверяет, соответствует ли сообщение контексту приложения (еда, кулинария, здоровье).
 * Работает без AI — только на основе ключевых слов.
 *
 * ВАЖНО: \b в JS-регулярках не работает с кириллицей (кириллица — не \w),
 * поэтому границы слов строим вручную: слева — не буква, справа — основы слов
 * матчатся как префиксы (полити → политика/политический), а у коротких или
 * опасных основ стоит явная правая граница (?![а-яё]), чтобы «рак» не ловил
 * «раков» (еда), а «суп» — «супер».
 */

// Левая граница слова: перед основой нет буквы/цифры
const L = '(?<![а-яёa-z0-9])'

function stems(fragments) {
  return new RegExp(L + '(?:' + fragments.join('|') + ')', 'i')
}

// Темы, которые явно не относятся к еде и кулинарии
const OFF_TOPIC_PATTERNS = [
  // Политика
  stems(['полити', 'президент', 'депутат', 'выбор[ыа]', 'парламент', 'санкц',
         'войн', 'нато(?![а-яё])', 'мвд(?![а-яё])', 'фсб(?![а-яё])', 'кремл',
         'оппозиц', 'протест', 'митинг', 'конституц']),
  // Погода
  stems(['погод[аыеу]', 'температура воздух', 'прогноз погод', 'осадк', 'климат',
         'ветер(?![а-яё])', 'дожд', 'снегопад', 'снег(?![а-яё])', 'мороз(?!ил)']),
  // Финансы / крипта
  stems(['биткоин', 'криптовалют', 'акци[ия]', 'бирж', 'инвестиц', 'форекс',
         'трейдинг', 'кредит', 'ипотек', 'банкрот', 'дефолт']),
  // Спорт (не связанный с питанием спортсменов)
  stems(['футбол', 'хоккей', 'баскетбол', 'теннис', 'формул[аы] 1', 'чемпионат',
         'матч', 'турнир', 'тренировк[аи] спорт']),
  // Новости / медиа
  stems(['новост', 'сми(?![а-яё])', 'телевидени', 'журналист', 'репортаж', 'пропаганд']),
  // Развлечения (кино, музыка — не еда)
  stems(['фильм', 'кино', 'сериал', 'акт[её]р', 'режисс[её]р', 'музыкант',
         'певец', 'певц', 'альбом', 'концерт', 'аниме']),
  // IT (не связанный с приложением)
  stems(['программирован', 'напиши код', 'python', 'javascript', 'sql запрос',
         'хакинг', 'взлом']),
  // Медицина (кроме питания)
  stems(['диагноз', 'таблетк', 'лечени', 'болезн', 'операци', 'антибиотик',
         'вирус гриппа', 'рак(?![а-яёa-z])', 'онколог']),
  // История / география (не кухня)
  stems(['история росси', 'вторая мировая', 'холодная война', 'столиц[аы] стран',
         'население стран']),
  // Юридические
  stems(['юрист', 'адвокат', 'суд(?![а-яё])', 'судебн', 'иск(?![а-яё])',
         'договор аренд', 'развод(?!и)', 'алимент']),
]

// Слова, которые делают сообщение точно релевантным — даже если есть спорные совпадения
const FOOD_SAFE_PATTERNS = [
  stems(['рецепт', 'блюд', 'ингредиент', 'приготов', 'готовить', 'готовлю', 'готовк',
         '(?:еда|еды|еде)(?![а-яё])', 'питани', 'кулинар', 'продукт', 'закуск',
         'салат', 'суп(?!ер)', 'десерт', 'завтрак', 'обед', 'ужин', 'перекус']),
  stems(['белк', 'жир(?!аф)', 'углевод', 'калори', 'кбжу', 'витамин', 'диет',
         'вегетари', 'веган', 'правильное питание', 'пп рецепт']),
  stems(['холодильн', 'морозилк', 'список покупок', 'что приготовить', 'что поесть',
         'что съесть', 'замен', 'из чего']),
]

/**
 * @param {string} message
 * @returns {{ allowed: boolean, reason?: string }}
 */
function checkMessageRelevance(message) {
  const text = message.trim()

  // Очень короткие сообщения пропускаем (продолжение диалога: "да", "нет", "спасибо")
  if (text.length < 15) return { allowed: true }

  // Если явно про еду — сразу разрешаем
  const isFoodRelated = FOOD_SAFE_PATTERNS.some(p => p.test(text))
  if (isFoodRelated) return { allowed: true }

  // Проверяем нерелевантные темы
  const offTopicMatch = OFF_TOPIC_PATTERNS.find(p => p.test(text))
  if (offTopicMatch) {
    return {
      allowed: false,
      reason: 'Я специализируюсь только на кулинарии, рецептах и питании. Задайте вопрос, связанный с едой.',
    }
  }

  // Нейтральные запросы — пропускаем (вопрос в контексте диалога)
  return { allowed: true }
}

module.exports = { checkMessageRelevance }
